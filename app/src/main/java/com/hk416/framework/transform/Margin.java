package com.hk416.framework.transform;

public final class Margin {
    public int top = 0;
    public int left = 0;
    public int bottom = 0;
    public int right = 0;

    public Margin() {
        /* empty */
    }

    public Margin(int top, int left, int bottom, int right) {
        this.top = top;
        this.left = left;
        this.bottom = bottom;
        this.right = right;
    }
}
